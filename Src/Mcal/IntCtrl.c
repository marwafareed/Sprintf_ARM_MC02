/******************************************************************************
* \Syntax          : void IntCrtl_Init(void)        
* \Description     : Initializes the interrupts                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*                                                                  
*******************************************************************************/
void IntCrtl_Init(void)
{
	/*APINT_Tag apint;*/
		
	/*Configure grouping/sub-grouping in APINT register in SCB */
	APINT.B.VECTKEY = 0x05FA;
	APINT.B.PRIGROUP |= INTCTRL_GROUP_FIELD_BIT_XX_Y;
 	
	/*Assign group/sub-group priority in NVIC_PRIx & SCB_Sys Registers*/
	/*port A priority interrupt*/
	PRI0 = INTA_MASK | 2; 
	
	/*Enable/Disable based on user configuration in NVIC_ENx and SCB_Sys registers*/
	/*Enable port A priority*/
	EN0 = INT_ENABLE;
}
