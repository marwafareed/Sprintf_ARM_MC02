/******************************************************************************
* \Syntax          : void ClockCtrl_Init(void)        
* \Description     : Initializes the Clock module according to the configuration set by the user in the config files                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*                                                                  
*******************************************************************************/
void ClockCtrl_Init(void)
{
	
	/*Select oscillator source (OSCSRC),*/
	#ifdef CLOCKCNTROL_INTERNAL
		RCC.B.OSCSRC = 0x1;				/*Internal*/
	#endif
	/*Bypass the PLL*/
	#ifdef CLOCKCNTROL_PLL_DISABLE
		RCC.B.BYPASS = 1;
	#endif
	/*open the clock gate for each peripheral*/
}
