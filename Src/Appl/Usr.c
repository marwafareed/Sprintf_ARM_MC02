/******************************************************************************
* \Syntax          : Usr_Set(Usr_OnTimeSecType on_time, Usr_OffTimeSecType off_time);     
* \Description     : Get the value of the on time and off time of the PWM from the user                                  
*                                                                             
* \Sync\Async      : Aynchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : None                     
* \Parameters (out): None                                                      
* \Return value:   : None
*                                                                  
*******************************************************************************/
void Usr_Set(Usr_OnTimeSecType on_time, Usr_OffTimeSecType off_time)
{
	if( (on_time <= USR_MAX_ON_TIME) && (off_time <= USR_MAX_OFF_TIME) )
	{
		PWM_SetPeriodSec(on_time,off_time);
	}else{
		/*Error in ON time or off time */
	}
	
}

