/******************************************************************************
* \Syntax          : void LED_Init(LED_NameType led)       
* \Description     : Initialize the LED module                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : LED_NameType  LED type to be intialized                     
* \Parameters (out): None                                                      
* \Return value:   : None                                
*******************************************************************************/
void LED_Init(LED_NameType led)
{
	/*Initialize the LED state*/
	switch(led)
	{
		case LED_PWM_OUT_PIN:
			Dio_WriteChannel(DIO_PORTF,1, DIO_LOW);
			break;
		default:
			/*You shouldn't be here*/
			break;
	}/*switch(led)*/
}

/******************************************************************************
* \Syntax          : void LED_SetState(LED_NameType led, LED_StateType state)        
* \Description     : Set the LED state; On or Off                                    
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : LED_NameType   LED type
										 LED_StateType  On or Off
* \Parameters (out): None                                                      
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK                                  
*******************************************************************************/
void LED_SetState(LED_NameType led, LED_StateType state)
{
	switch(led)
	{
		case LED_PWM_OUT_PIN:
			Dio_WriteChannel(DIO_PORTF,1, (Dio_LevelType)state);
			break;
		default:
			/*You shouldn't be here*/
			break;
	}/*switch(led)*/
}


